import { cn } from "@/lib/utils";
import "./globals.css";
import { Inter } from "next/font/google";
import Providers from "../lib/providers";
import Navbar from "@/components/Navbar";
import { db } from "@/lib/turso";
import { session } from "@/lib/schema";

const inter = Inter({ subsets: ["latin"] });

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const sessions: { title: string; href: number; description: string }[] =
    await db
      .select()
      .from(session)
      .all()
      .then((data) =>
        data.map((s) => ({
          title: s.title,
          href: s.id,
          description: s.description || "",
        }))
      );

  return (
    <html lang="en">
      <body className={cn(inter.className, "tracking-tight dark")}>
        {/* change bg color later  */}
        {/* <nav className="sticky top-0 flex justify-around border-b py-5 backdrop-blur-sm backdrop-brightness-50"> */}
        {/* <Link href="/" className="text-xl font-bold">
            home
          </Link>
          <Link href="/exercises" className="text-xl font-bold">
            exercises
          </Link>
          <Link href="/sessions" className="text-xl font-bold">
            sessions
          </Link> */}
        {/* </nav> */}
        <Providers>
          <Navbar sessions={sessions} />
          {children}
        </Providers>
      </body>
    </html>
  );
}

import "./globals.css";
import Navbar from "@/components/Navbar";
import NavbarSkeleton from "@/components/NavbarSkeleton";
import { Suspense } from "react";
import { Inter } from "next/font/google";
import { ClerkProvider } from "@clerk/nextjs";
import { dark } from "@clerk/themes";
import { Analytics } from "@vercel/analytics/react";
import { Providers } from "./providers";
import { cn } from "@/lib/utils";
import { Toaster } from "@/components/ui/toaster";
import { getLogs, getTemplates, getWorkouts } from "@/app/(pages)/actions";

const inter = Inter({ subsets: ["latin"] });

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <ClerkProvider
      appearance={{
        baseTheme: dark,
      }}
    >
      <html lang="en">
        <body className={cn(inter.className, "tracking-tight")}>
          <Providers>
            <div className="grid min-h-screen items-start">
              <Suspense fallback={<NavbarSkeleton />}>
                <FetchNavbarData />
              </Suspense>
              <div>
                {children}
                <Toaster />
                <Analytics />
              </div>
            </div>
          </Providers>
        </body>
      </html>
    </ClerkProvider>
  );
}

async function FetchNavbarData() {
  const [templates, workouts, logs] = await Promise.all([
    getTemplates(),
    getWorkouts(),
    getLogs(),
  ]);

  return (
    <Navbar
      initialTemplates={templates}
      initialWorkouts={workouts}
      initialLogs={logs}
    />
  );
}
